def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter


def area_of_square(width):
    area = width**2
    return area


def perimeter_of_square(width):
    perimeter = 4 * width
    return perimeter

def volume_of_cube(width):
    volume = width**3
    return volume

def multiplication(x, y):
    res = x*y
    return res
def sum(x,y):
    res = x+y
    return res
def sub(x,y):
    res = x-y
    return res
def division(x,y):
    res = x/y
    return res
